export function isBlocked(url: string, userBlocklist: string[]): boolean {
  const fullList = [...defaultBlocklist, ...userBlocklist];
  try {
    const hostname = new URL(url).hostname.replace("www.", "");
    return fullList.some(
      (blocked) => hostname === blocked || hostname.endsWith("." + blocked)
    );
  } catch {
    return false;
  }
}
